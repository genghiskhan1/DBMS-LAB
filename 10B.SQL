SET SERVEROUTPUT ON

DROP TABLE EMPLOYEE;

CREATE TABLE EMPLOYEE (
SSN NUMBER(10) PRIMARY KEY,
NAME VARCHAR(10) NOT NULL,
SAL NUMBER(10) NOT NULL,
DEPTNO NUMBER(10) NOT NULL
);

INSERT INTO EMPLOYEE VALUES(1,'RAM',10000,10);
INSERT INTO EMPLOYEE VALUES(2,'HARAM',20000,10);
INSERT INTO EMPLOYEE VALUES(3,'GARAM',10000,11);
INSERT INTO EMPLOYEE VALUES(4,'SHARAM',20000,10);

SELECT *FROM EMPLOYEE;

DECLARE
CURSOR CURR IS 
SELECT *FROM EMPLOYEE;
TEMP CURR%ROWTYPE;
T INT:=&T1;

BEGIN
OPEN CURR;
LOOP
FETCH CURR INTO TEMP;
EXIT WHEN CURR%NOTFOUND;

IF (TEMP.DEPTNO=T) THEN
DBMS_OUTPUT.PUT_LINE(TEMP.SSN||' '||TEMP.NAME);
END IF;
END LOOP;
CLOSE CURR;
END;
