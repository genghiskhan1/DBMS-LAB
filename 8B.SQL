SET SERVEROUTPUT ON

DECLARE
N1 NUMBER(5);
N2 NUMBER(5);
EBIGGER EXCEPTION;

BEGIN
N1:=&SV1;
N2:=&SV2;

IF(N1<N2) THEN
DBMS_OUTPUT.PUT_LINE(' NO ERROR AS N1 SMALLER THAN N2 ');
ELSE
RAISE EBIGGER;
END IF;

EXCEPTION WHEN EBIGGER THEN
DBMS_OUTPUT.PUT_LINE('EXCEPTION CAUGHT,N2 SMALLER THAN N1');

END;
