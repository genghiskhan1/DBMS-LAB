SET SERVEROUTPUT ON

DECLARE 
REV INT:=0;
DIG INT;
TEMP INT;
NUM INT:=&N1;

BEGIN
TEMP:=NUM;
WHILE(TEMP!=0) LOOP
DIG:=MOD(TEMP,10);
REV:=(REV*10)+DIG;
TEMP:=TEMP/10;
END LOOP;

IF(REV=NUM) THEN
DBMS_OUTPUT.PUT_LINE(NUM||' IS A PALINDROME ');
ELSE
DBMS_OUTPUT.PUT_LINE(NUM||' IS NOT A PALINDROME');
END IF;
END;
/