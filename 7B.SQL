CREATE TABLE NEW_SAILOR (
SID NUMBER(10) PRIMARY KEY,
SNAME VARCHAR(10) NOT NULL,
AGE NUMBER(10) NOT NULL
);

DESC SAILOR;
DESC NEW_SAILOR;

SELECT *FROM SAILOR;

DECLARE
CURSOR CURR IS
SELECT *FROM SAILOR;
TEMP CURR%ROWTYPE;
COUNTER INT;

BEGIN
OPEN CURR;
LOOP
FETCH CURR INTO TEMP;
EXIT WHEN CURR%NOTFOUND;

INSERT INTO NEW_SAILOR VALUES(TEMP.SID,TEMP.SNAME,TEMP.AGE);

END LOOP;
COUNTER:=CURR%ROWCOUNT;
DBMS_OUTPUT.PUT_LINE('NUMBER OF ROWS COPIED: '||COUNTER);
CLOSE CURR;
END;
/

